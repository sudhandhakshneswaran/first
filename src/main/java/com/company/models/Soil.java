package com.company.models;

public class Soil {

	private int soilId;
	private String soilName;

	public Soil() {
		super();
	}

	public int getSoilId() {
		return soilId;
	}

	public void setSoilId(int soilId) {
		this.soilId = soilId;
	}

	public String getSoilName() {
		return soilName;
	}

	public void setSoilName(String soilName) {
		this.soilName = soilName;
	}

}
